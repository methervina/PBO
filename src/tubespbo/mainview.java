        String jenis = null;
        if(rdreguler.isSelected()){
            jenis = "Reguler";
        }else if(rdpremium.isSelected()){
            jenis = "Premium";
        }
        String sql = "insert into formorder values (?,?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, lblnama_order.getText());
            stat.setString(2, lblnohp_order.getText());
            stat.setString(3, lblalamat_order.getText());
            stat.setString(4, String.valueOf(pilihproduk.getSelectedItem()));
            stat.setString(5, jenis);
            stat.setString(6, totalharga.getText());
            
            stat.executeUpdate();
            
            kosong();
        
        }catch (SQLException e){
            
        }datatable();
        datatable();
