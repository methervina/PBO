    protected void datatable(){
        String[] Data ={"Produk", "Kualitas", "Harga"};
        tabmode = new DefaultTableModel(null, Data);
        datatable.setModel(tabmode);
        String sql = "select*from formorder";
        
        try{
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String pilihproduk = hasil.getString("Produk");
                String pilihkualitas = hasil.getString("Kualitas");
                String totalharga = hasil.getString("Total Harga");
                String[] DataOrder = {pilihproduk, pilihkualitas, totalharga};
                tabmode.addRow(DataOrder);
            }
            datatable.setModel(tabmode);
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "data gagal dipanggil"+e);
        }
    }
    

